def valida_senha():
  lista_numero = "0123456789"
  lista_caractere_especiais = "!@#$%¨&*()_+"

  senha_digitada = str(input("Digite sua senha....: "))

  tem_numero = False
  tem_caractere_especial = False

  print(senha_digitada)

  for caracter in senha_digitada:
    if caracter in lista_numero:
       tem_numero = True
       break
    else:
      tem_numero = False 

  for caractere in senha_digitada:
    if caractere in lista_caractere_especiais:
      tem_caractere_especiais = True
      break
    else: 
      tem_caractere_especiais = True

    if tem_caractere_especial == True and tem_numero == True:
      print("Senha válida")
    else:
      print("Senha INVALIDA!")

if __name__ == "__main__":
  valida_senha()

