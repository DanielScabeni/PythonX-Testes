  def __init__(self, pessoa, equipamento, dias):
    self.pessoa = pessoa
    self.equipamento = equipamento

    if(dias):
       d = datetime.today() + timedelta(dias)
       self.data_devolucao = d
    else:
      d = datetime.today() + timedelta(equipamento.tempo_locacao)
